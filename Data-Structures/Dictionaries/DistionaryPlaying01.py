# Simple playing with dictionaries

car = {"make":"Toyota", "model":"Corolla", "year":2020}
print ("Original dictionary: ", car)

car["color"] = "red"
print ("Dictionary after adding a new key/value pair color:red ", car)

print("Printing the model:", car["model"])

car["year"] = 2021
print ("Dictionary after updating the year to 2021 ", car)
